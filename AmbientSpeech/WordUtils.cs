        internal static IEnumerable<WordPayload> GetCleansedWords(this string text)
        {
            DateTime dtm = DateTime.Now;

            var payloads = from word in text.Split(new char[] { ' ' })
                           let cleanWord = CleanWord(word)
                           where stopWords.FirstOrDefault(w => w == cleanWord) == null
                           select new WordPayload() { Word = cleanWord, WordTime = dtm };

            return payloads;
        }

        /// <summary>
        /// * strips puncuation
        /// * to lower case
        /// </summary>
        /// <param name="word"></param>
        /// <returns></returns>
        private static string CleanWord(string word) =>
            new string(word.Where(c => !char.IsPunctuation(c)).ToArray()).ToLower();
